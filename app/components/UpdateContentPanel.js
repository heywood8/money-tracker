const DATE_RE = /(\d{4}-\d{2}-\d{2})/;
const PR_RE = /#(\d+)/g;

const stripMarkdown = (md) => md
  .replace(/\r\n/g, '\n')
  .replace(/#{1,6}\s+/g, '')
  .replace(/\*\*(.+?)\*\*/g, '$1')
  .replace(/\*(.+?)\*/g, '$1')
  .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
  .replace(/`([^`]+)`/g, '$1')
  .replace(/^\s*[-*+]\s+/gm, '• ')
  .replace(/\n{3,}/g, '\n\n')
  .trim();

// Splits the release body off from its leading "version (date)" heading.
// Release notes generated by semantic-release start with a markdown heading such as
// "## 0.134.15 (2026-06-17)" which duplicates the version we already show. We lift the
// date out of that heading (to render it inline beside the version) and drop the line
// from the body so it isn't shown twice.
export const parseReleaseNotes = (notes, version) => {
  const cleaned = (notes || '').replace(/\r\n/g, '\n');
  const lines = cleaned.split('\n');

  let date = null;
  let firstContentIdx = -1;
  for (let i = 0; i < lines.length; i += 1) {
    if (lines[i].trim()) {
      firstContentIdx = i;
      break;
    }
  }

  let bodyLines = lines;
  if (firstContentIdx >= 0) {
    const firstLine = lines[firstContentIdx];
    const trimmed = firstLine.trim();
    const isHeading = /^#{1,6}\s/.test(firstLine);
    const hasDate = DATE_RE.test(trimmed);
    const hasVersion = version && trimmed.includes(version);
    if (isHeading && (hasDate || hasVersion)) {
      const match = trimmed.match(DATE_RE);
      if (match) {
        [, date] = match;
      }
      bodyLines = lines.slice(firstContentIdx + 1);
    }
  }

  return { date, body: stripMarkdown(bodyLines.join('\n')) };
};

// Derives the repository base URL (e.g. https://github.com/owner/repo) from the
// releases URL so we can build links to individual pull requests.
const repoBaseFromReleasesUrl = (releasesUrl) => {
  if (!releasesUrl || typeof releasesUrl !== 'string') return null;
  return releasesUrl.replace(/\/releases\/?$/, '');
};

// Renders release-note text, turning "#123" pull-request references into tappable links.
const renderNotesWithLinks = (text, repoBase, linkColor) => {
  if (!text) return null;
  if (!repoBase) return text;

  const segments = [];
  let lastIndex = 0;
  let match;
  PR_RE.lastIndex = 0;
  while ((match = PR_RE.exec(text)) !== null) {
    if (match.index > lastIndex) {
      segments.push(text.slice(lastIndex, match.index));
    }
    const prNumber = match[1];
    segments.push(
      <Text
        key={`pr-${match.index}`}
        style={[styles.prLink, { color: linkColor }]}
        onPress={() => Linking.openURL(`${repoBase}/pull/${prNumber}`)}
        accessibilityRole="link"
        accessibilityLabel={`Pull request ${prNumber}`}
      >
        #{prNumber}
      </Text>,
    );
    lastIndex = match.index + match[0].length;
  }
  if (lastIndex < text.length) {
    segments.push(text.slice(lastIndex));
  }
  return segments;
};

const formatApkDate = (modificationTime) => {
  if (!modificationTime) return '';
  return new Date(modificationTime * 1000).toLocaleDateString(undefined, { month: 'short', day: 'numeric' });
};

// Green used for the "installed / up to date" status, matching the checkmark elsewhere in this panel.
const INSTALLED_GREEN = '#4caf50';
// Amber used to warn that a corrupt cached download was discarded and is being re-downloaded.
const WARNING_AMBER = '#f0a500';

function ReleaseCard({ version, notes, badge, buildProgress, matchedApk, repoBase, onInstallApk, colors, t, isInstalled, isLatestInstalled, isUpdateCandidate }) {
  const { date, body } = parseReleaseNotes(notes, version);
  const spinAnim = useRef(new Animated.Value(0)).current;
  useEffect(() => {
    if (!buildProgress) return undefined;
    const loop = Animated.loop(
      Animated.timing(spinAnim, { toValue: 1, duration: 1500, easing: Easing.linear, useNativeDriver: true }),
    );
    loop.start();
    return () => { loop.stop(); spinAnim.setValue(0); };
  }, [buildProgress, spinAnim]);
  // We highlight a single card: the candidate for installation when an update is available,
  // otherwise the installed-and-latest card when we are already up to date. The candidate
  // takes the primary accent; the up-to-date installed card keeps the green "latest" accent.
  const highlighted = isUpdateCandidate || isLatestInstalled;
  const accent = isLatestInstalled ? INSTALLED_GREEN : colors.primary;
  // Once an update is available the installed card is no longer the highlighted one, so its
  // "Installed" chip recedes to a muted, purely informational treatment.
  const installedChipColor = isLatestInstalled ? accent : colors.mutedText;
  return (
    <View
      style={[
        styles.releaseCard,
        { backgroundColor: colors.surface, borderColor: highlighted ? accent : colors.border },
        highlighted && styles.releaseCardHighlighted,
      ]}
    >
      <View style={styles.releaseHeader}>
        <View style={styles.releaseHeaderLeft}>
          <Text style={[styles.releaseVersion, { color: colors.text }]}>v{version}</Text>
          {date ? (
            <Text style={[styles.releaseDate, { color: colors.mutedText }]}>{date}</Text>
          ) : null}
          {badge ? (
            <Text style={[styles.releaseBadge, { color: colors.mutedText, borderColor: colors.border }]}>
              {badge}
            </Text>
          ) : null}
          {buildProgress && typeof buildProgress.percent === 'number' ? (
            <View
              style={[styles.buildProgressChip, { borderColor: colors.primary }]}
              accessibilityRole="text"
              accessibilityLabel={(t('build_in_progress') || 'Building {percent}%').replace('{percent}', String(buildProgress.percent))}
            >
              <Animated.View style={{ transform: [{ rotate: spinAnim.interpolate({ inputRange: [0, 1], outputRange: ['0deg', '360deg'] }) }] }}>
                <Ionicons name="sync-outline" size={13} color={colors.primary} />
              </Animated.View>
              <Text style={[styles.buildProgressText, { color: colors.primary }]}>
                {(t('build_in_progress') || 'Building {percent}%').replace('{percent}', String(buildProgress.percent))}
              </Text>
            </View>
          ) : null}
          {isUpdateCandidate ? (
            <View
              style={[styles.installedChip, { borderColor: colors.primary }]}
              accessibilityRole="text"
              accessibilityLabel={t('update_candidate') || 'Available to install'}
            >
              <Ionicons name="arrow-up-circle" size={13} color={colors.primary} />
              <Text style={[styles.installedChipText, { color: colors.primary }]}>
                {t('update_candidate') || 'Available'}
              </Text>
            </View>
          ) : null}
          {isInstalled ? (
            <View
              style={[styles.installedChip, { borderColor: installedChipColor }]}
              accessibilityRole="text"
              accessibilityLabel={isLatestInstalled
                ? (t('installed_latest_hint') || 'Installed — you are on the latest version')
                : (t('installed') || 'Installed')}
            >
              {isLatestInstalled ? (
                <Ionicons name="checkmark-circle" size={13} color={accent} />
              ) : null}
              <Text style={[styles.installedChipText, { color: installedChipColor }]}>
                {t('installed') || 'Installed'}
              </Text>
            </View>
          ) : null}
        </View>
        {matchedApk ? (
          <TouchableOpacity
            onPress={() => onInstallApk(matchedApk.uri)}
            style={styles.releaseApkButton}
            accessibilityRole="button"
            accessibilityLabel={`Install version ${version}`}
          >
            <Ionicons name="archive-outline" size={18} color={colors.primary} />
            <Ionicons name="download-outline" size={14} color={colors.primary} />
          </TouchableOpacity>
        ) : null}
      </View>
      {isLatestInstalled ? (
        <View style={styles.installedHintRow}>
          <Ionicons name="information-circle-outline" size={13} color={INSTALLED_GREEN} />
          <Text style={[styles.installedHintText, { color: INSTALLED_GREEN }]}>
            {t('installed_latest_hint') || "You're on the latest version."}
          </Text>
        </View>
      ) : null}
      {body ? (
        <Text style={[styles.releaseBody, { color: colors.text }]}>
          {renderNotesWithLinks(body, repoBase, colors.primary)}
        </Text>
      ) : null}
    </View>
  );
}

