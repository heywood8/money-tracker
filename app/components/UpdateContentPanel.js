// Compact list of downloaded APKs that did not line up with a release shown above.
function UnmatchedApks({ apks, onInstallApk, colors, t, compact }) {
  if (!apks.length) return null;
  return (
    <View style={compact ? styles.downloadedApksCompact : styles.downloadedApksSection}>
      <Text style={[compact ? styles.downloadedApksTitleCompact : styles.downloadedApksTitle, { color: colors.mutedText }]}>
        {t('downloaded_apks') || 'Downloaded APKs'}
      </Text>
      <ScrollView horizontal showsHorizontalScrollIndicator={false} contentContainerStyle={styles.apkListContent}>
        {apks.map((item) => (
          <TouchableOpacity
            key={item.uri}
            onPress={() => onInstallApk(item.uri)}
            style={styles.apkChipCompact}
            accessibilityRole="button"
            accessibilityLabel={`Install version ${item.version || item.filename}`}
          >
            <Ionicons name="archive-outline" size={22} color={colors.primary} />
            <Text style={[styles.apkChipVersion, { color: colors.text }]}>
              {item.version ? `v${item.version}` : item.filename.replace(/\.apk$/i, '')}
            </Text>
            <Text style={[styles.apkChipDate, { color: colors.mutedText }]}>
              {formatApkDate(item.modificationTime)}
            </Text>
          </TouchableOpacity>
        ))}
      </ScrollView>
    </View>
  );
}

UnmatchedApks.propTypes = {
  apks: PropTypes.array,
  onInstallApk: PropTypes.func,
  colors: PropTypes.object,
  t: PropTypes.func,
  compact: PropTypes.bool,
};

const buildApkLookup = (downloadedApks) => {
  const map = new Map();
  downloadedApks.forEach((apk) => {
    if (apk.version && !map.has(apk.version)) {
      map.set(apk.version, apk);
    }
  });
  return map;
};

const unmatchedApksFor = (downloadedApks, releases) => {
  const shown = new Set(releases.map((r) => r.version));
  return downloadedApks.filter((apk) => !apk.version || !shown.has(apk.version));
};

export default function UpdateContentPanel({ isChecking, updateResult, downloadedApks, onUpdate, onInstallApk, onRefresh }) {
  const { colors } = useThemeColors();
  const { t } = useLocalization();
  const contentAnim = useRef(new Animated.Value(0)).current;

  useEffect(() => {
    if (isChecking) {
      contentAnim.setValue(0);
    }
  }, [isChecking, contentAnim]);

  useEffect(() => {
    if (!isChecking && updateResult) {
      Animated.timing(contentAnim, {
        toValue: 1,
        duration: 280,
        easing: Easing.out(Easing.quad),
        useNativeDriver: true,
      }).start();
    }
  }, [isChecking, updateResult, contentAnim]);

  if (isChecking) {
    return (
      <View style={styles.checkingContainer}>
        <ActivityIndicator size="large" color={colors.primary} />
        <Text style={[styles.checkingText, { color: colors.text }]}>
          {t('checking_for_updates') || 'Checking for available updates…'}
        </Text>
      </View>
    );
  }

  if (!updateResult) return null;

  const repoBase = repoBaseFromReleasesUrl(updateResult.releasesUrl);
  const apkLookup = buildApkLookup(downloadedApks);

  // The currently installed app version. We treat it as the latest — green checkmark + hint —
  // whenever there is no newer *installable* release. That covers two cases: being fully up to
  // date, and a newer release existing but having no APK attached yet (CI build still running).
  // In the latter case the user still holds the newest installable APK, so the installed version
  // is the latest one they can actually run.
  const installedVersion = updateResult.currentVersion;
  const installedIsLatest = updateResult.type === 'up_to_date'
    || (updateResult.type === 'error' && updateResult.errorCode === 'releases_without_apks');
  // When an update is available, the release we want the user to install is the highlighted
  // one — not the version they already have.
  const candidateVersion = updateResult.type === 'available' ? updateResult.latestVersion : null;

  const renderReleaseCards = (releases) => releases.map((release) => {
    const isInstalled = !!installedVersion && release.version === installedVersion;
    const isUpdateCandidate = !!candidateVersion && release.version === candidateVersion;
    return (
      <ReleaseCard
        key={release.version}
        version={release.version}
        notes={release.notes}
        badge={release.badge}
        buildProgress={release.buildProgress}
        matchedApk={apkLookup.get(release.version)}
        repoBase={repoBase}
        onInstallApk={onInstallApk}
        colors={colors}
        t={t}
        isInstalled={isInstalled}
        isLatestInstalled={isInstalled && installedIsLatest}
        isUpdateCandidate={isUpdateCandidate}
      />
    );
  });

  return (
    <Animated.View style={[styles.resultContainer, { opacity: contentAnim }]}>
      {updateResult.type === 'available' && (() => {
        const releaseNotes = updateResult.recentReleaseNotes || updateResult.releaseNotes;
        const releases = releaseNotes || [];
        const unmatched = unmatchedApksFor(downloadedApks, releases);
        return (
          <>
            {releaseNotes ? (
              <>
                <Text style={[styles.changelogTitle, { color: colors.mutedText }]}>
                  {t('whats_new') || "What's new"}
                </Text>
                <ScrollView
                  style={styles.changelogScroll}
                  showsVerticalScrollIndicator={false}
                  refreshControl={onRefresh ? <RefreshControl refreshing={false} onRefresh={onRefresh} /> : undefined}
                >
                  {renderReleaseCards(releases)}
                  {updateResult.releasesUrl && (
                    <MoreReleasesLink url={updateResult.releasesUrl} colors={colors} t={t} />
                  )}
                </ScrollView>
              </>
            ) : (
              <Text style={[styles.updateVersionText, { color: colors.mutedText }]}>
                {t('update_install_hint') || 'If installation is blocked, allow "Install unknown apps" for your browser or file manager in Android settings.'}
              </Text>
            )}
            <Divider style={styles.updateDivider} />
            {updateResult.previousDownloadCorrupted ? (
              <View style={styles.corruptNoteRow} accessibilityRole="text">
                <Ionicons name="alert-circle-outline" size={14} color={WARNING_AMBER} />
                <Text style={[styles.corruptNoteText, { color: WARNING_AMBER }]}>
                  {t('apk_corrupt_redownload') || 'The previous download was damaged and was removed. Tap Update now to download it again.'}
                </Text>
              </View>
            ) : null}
            <View style={styles.updateBottomRow}>
              {releaseNotes ? (
                <UnmatchedApks apks={unmatched} onInstallApk={onInstallApk} colors={colors} t={t} compact />
              ) : (
                <UnmatchedApks apks={downloadedApks} onInstallApk={onInstallApk} colors={colors} t={t} compact />
              )}
              <View style={styles.updateActionColumn}>
                <Text style={[styles.updateButtonVersion, { color: colors.text }]}>
                  v{updateResult.latestVersion}
                </Text>
                <Text style={[styles.updateButtonCurrentVersion, { color: colors.mutedText }]}>
                  {(t('update_from_version') || 'installed: v{currentVersion}')
                    .replace('{currentVersion}', updateResult.currentVersion)}
                </Text>
                <TouchableRipple
                  onPress={() => {
                    if (updateResult.alreadyDownloaded) {
                      onInstallApk(updateResult.localUri);
                    } else {
                      onUpdate(updateResult.downloadUrl, updateResult.checksumUrl);
                    }
                  }}
                  style={[styles.updateButtonCompact, { backgroundColor: colors.primary }]}
                >
                  <Text style={styles.updateButtonText}>
                    {updateResult.alreadyDownloaded
                      ? (t('update_install_now') || 'Install now')
                      : (t('update_now') || 'Update now')}
                  </Text>
                </TouchableRipple>
              </View>
            </View>
          </>
        );
      })()}

      {updateResult.type === 'up_to_date' && (
        <View style={styles.upToDateContent}>
          {updateResult.recentReleaseNotes ? (() => {
            const releases = updateResult.recentReleaseNotes;
            const unmatched = unmatchedApksFor(downloadedApks, releases);
            // The installed version is also the latest one here, so it is rendered as a
            // green-highlighted card carrying the "you're on the latest version" hint inline.
            // Only fall back to a standalone bottom status when that card is absent (the
            // installed version isn't among the listed releases).
            const latestShownOnCard = !!installedVersion && releases.some((r) => r.version === installedVersion);
            return (
              <>
                <Text style={[styles.changelogTitle, { color: colors.mutedText }]}>
                  {t('release_history') || 'Release history'}
                </Text>
                <ScrollView
                  style={styles.changelogScroll}
                  showsVerticalScrollIndicator={false}
                  refreshControl={onRefresh ? <RefreshControl refreshing={false} onRefresh={onRefresh} /> : undefined}
                >
                  {renderReleaseCards(releases)}
                  {updateResult.releasesUrl && (
                    <MoreReleasesLink url={updateResult.releasesUrl} colors={colors} t={t} />
                  )}
                </ScrollView>
                {(unmatched.length > 0 || !latestShownOnCard) && (
                  <>
                    <Divider style={styles.updateDivider} />
                    <View style={styles.updateBottomRow}>
                      <UnmatchedApks apks={unmatched} onInstallApk={onInstallApk} colors={colors} t={t} compact />
                      {!latestShownOnCard ? (
                        <View style={styles.upToDateBottomRight}>
                          <Ionicons name="checkmark-circle-outline" size={24} color="#4caf50" />
                          <Text style={[styles.upToDateHeaderText, { color: colors.text }]}>
                            {t('up_to_date') || 'You already have the latest version installed.'}
                          </Text>
                        </View>
                      ) : null}
                    </View>
                  </>
                )}
              </>
            );
          })() : (
            <>
              <View style={styles.upToDateHeaderSpacer}>
                <View style={styles.upToDateHeader}>
                  <Ionicons name="checkmark-circle-outline" size={48} color="#4caf50" style={styles.centeredIcon} />
                  <Text style={[styles.updateVersionText, { color: colors.text }]}>
                    {t('up_to_date') || 'You already have the latest version installed.'}
                  </Text>
                </View>
              </View>
              {downloadedApks.length > 0 && (
                <View style={styles.downloadedApksSection}>
                  <Divider />
                  <UnmatchedApks apks={downloadedApks} onInstallApk={onInstallApk} colors={colors} t={t} />
                </View>
              )}
            </>
          )}
        </View>
      )}

      {updateResult.type === 'error' && updateResult.errorCode === 'releases_without_apks' && (updateResult.releaseNotes || updateResult.recentReleaseNotes) ? (() => {
        // Surface CI build progress on the newest release awaiting its APK — that is the build
        // currently running. Older no-APK releases (if any) get no progress chip.
        let buildProgressShown = false;
        const noApkReleases = (updateResult.releaseNotes || []).map((r) => {
          const showProgress = !r.hasApk && !buildProgressShown && !!updateResult.buildProgress;
          if (showProgress) buildProgressShown = true;
          return {
            version: r.version,
            notes: r.notes,
            badge: !r.hasApk ? (t('no_apk_attached') || 'NO_APK_ATTACHED') : undefined,
            buildProgress: showProgress ? updateResult.buildProgress : null,
          };
        });
        const recentReleases = updateResult.recentReleaseNotes || [];
        return (
          <View style={styles.upToDateContent}>
            <Text style={[styles.changelogTitle, { color: colors.mutedText }]}>
              {t('release_history') || 'Release history'}
            </Text>
            <ScrollView
              style={styles.changelogScroll}
              showsVerticalScrollIndicator={false}
              refreshControl={onRefresh ? <RefreshControl refreshing={false} onRefresh={onRefresh} /> : undefined}
            >
              {renderReleaseCards(noApkReleases)}
              {renderReleaseCards(recentReleases)}
              {updateResult.releasesUrl && (
                <MoreReleasesLink url={updateResult.releasesUrl} colors={colors} t={t} />
              )}
            </ScrollView>
          </View>
        );
      })() : updateResult.type === 'error' && (
        <View style={styles.errorContent}>
          <Ionicons name="cloud-offline-outline" size={48} color={colors.mutedText} style={styles.centeredIcon} />
          <Text style={[styles.updateVersionText, { color: colors.text }]}>
            {updateResult.errorCode === 'releases_without_apks'
              ? (t('update_releases_without_apks') || 'Found releases but no APKs attached. Check GitHub for the latest release.')
              : (t('update_check_failed') || 'Could not check updates right now. Please try again later.')}
          </Text>
        </View>
      )}
    </Animated.View>
  );
}

